// Language management utilities
export const getCurrentLanguage = () => {
  return localStorage.getItem('language') || 'fr';
};

export const setCurrentLanguage = (lang) => {
  localStorage.setItem('language', lang);
  document.documentElement.lang = lang;
  
  // Add RTL support for Arabic
  if (lang === 'ar') {
    document.body.classList.add('lang-ar');
    document.documentElement.dir = 'rtl';
  } else {
    document.body.classList.remove('lang-ar');
    document.documentElement.dir = 'ltr';
  }
};

export const getTranslation = (key, lang = getCurrentLanguage()) => {
  return content[lang]?.[key] || content.fr[key] || key;
};

export const updateContent = (lang) => {
  setCurrentLanguage(lang);
  
  // Update all elements with data-translate-key attribute
  const elements = document.querySelectorAll('[data-translate-key]');
  elements.forEach(element => {
    const key = element.getAttribute('data-translate-key');
    const translation = getTranslation(key, lang);
    
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      element.placeholder = translation;
    } else {
      element.textContent = translation;
    }
  });
  
  // Trigger custom event for components that need to update
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}; 